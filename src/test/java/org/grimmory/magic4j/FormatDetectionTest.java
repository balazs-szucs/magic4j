  /**
   * Verifies all audio formats used by Grimmory satisfy {@code isAudio(mime)} which is implemented
   * as {@code mime != null && mime.startsWith("audio/")}. Uses file-based detection because
   * Grimmory detects files from disk.
   */
  @Test
  void allAudioFormatsStartWithAudio(@TempDir Path tmpDir) throws Exception {
    record Format(String name, byte[] bytes, String ext) {}

    var formats =
        List.of(
            new Format("MP3", buildMp3Bytes(), ".mp3"),
            new Format("FLAC", FLAC, ".flac"),
            new Format("WAV", buildWavBytes(), ".wav"),
            new Format("AIFF", buildAiffBytes(), ".aiff"),
            new Format("M4A", buildM4aBytes(), ".m4a"),
            new Format("M4B", buildM4bBytes(), ".m4b"),
            new Format("OGG/Opus", buildOpusBytes(), ".opus"));
    for (Format f : formats) {
      Path file = tmpDir.resolve("test" + f.ext());
      Files.write(file, f.bytes());
      String mime = Magic.detectMimeType(file);
      assertTrue(mime.startsWith("audio/"), f.name() + " failed isAudio() contract. Got: " + mime);
    }
  }

