  /** Returns a complete minimal PCM WAV file generated by javax.sound.sampled. */
  private static byte[] buildWavBytes() throws Exception {
    ByteArrayOutputStream baos = new ByteArrayOutputStream();
    AudioFormat fmt = new AudioFormat(44100, 16, 2, true, false);
    byte[] silence = new byte[4]; // 1 stereo sample = 4 bytes
    try (AudioInputStream ais = new AudioInputStream(new ByteArrayInputStream(silence), fmt, 1)) {
      AudioSystem.write(ais, AudioFileFormat.Type.WAVE, baos);
    }
    return baos.toByteArray();
  }

  /** Returns a complete minimal AIFF file generated by javax.sound.sampled. */
  private static byte[] buildAiffBytes() throws Exception {
    ByteArrayOutputStream baos = new ByteArrayOutputStream();
    AudioFormat fmt = new AudioFormat(44100, 16, 2, true, true); // big-endian for AIFF
    byte[] silence = new byte[4];
    try (AudioInputStream ais = new AudioInputStream(new ByteArrayInputStream(silence), fmt, 1)) {
      AudioSystem.write(ais, AudioFileFormat.Type.AIFF, baos);
    }
    return baos.toByteArray();
  }

  /**
   * Returns a raw MPEG-1 Layer III byte sequence spanning two complete sync frames. Frame size for
   * 128kbps/44100Hz/stereo = 417 bytes. libmagic validates two consecutive sync headers before
   * committing to audio/mpeg.
   */
  private static byte[] buildMp3Bytes() {
    byte[] b = new byte[834]; // 2 × 417 bytes (two 128kbps stereo frames)
    // Frame 1 header: MPEG-1, Layer III, 128kbps, 44100Hz, stereo (joint stereo = 0x90)
    b[0] = (byte) 0xFF;
    b[1] = (byte) 0xFB;
    b[2] = (byte) 0x90;
    b[3] = 0x00;
    // Frame 2 header at offset 417
    b[417] = (byte) 0xFF;
    b[418] = (byte) 0xFB;
    b[419] = (byte) 0x90;
    b[420] = 0x00;
    return b;
  }

  /** Returns a minimal BMP file with a complete BITMAPINFOHEADER (54 bytes, 1×1 pixel, 24bpp). */
  private static byte[] buildBmpBytes() {
    byte[] b = new byte[54];
    b[0] = 'B';
    b[1] = 'M'; // signature
    b[2] = 54; // file size (little-endian, fits in 1 byte here)
    b[10] = 54; // pixel data offset
    b[14] = 40; // BITMAPINFOHEADER size = 40
    b[18] = 1; // width = 1
    b[22] = 1; // height = 1
    b[26] = 1; // color planes = 1
    b[28] = 24; // bits per pixel = 24
    return b;
  }

