  /**
   * Returns a minimal OGG/Opus page (47 bytes) with a valid OpusHead identification header.
   * libmagic identifies the OGG container regardless of the codec carried inside.
   */
  private static byte[] buildOpusBytes() {
    byte[] b = new byte[47];
    b[0] = 0x4F;
    b[1] = 0x67;
    b[2] = 0x67;
    b[3] = 0x53; // "OggS"
    b[4] = 0x00; // stream structure version
    b[5] = 0x02; // header type: beginning-of-stream
    // granule position [6..13] = 0
    // serial/sequence numbers [14..21] = 0
    // checksum [22..25] = 0 (CRC not validated for content detection)
    b[26] = 0x01; // page_segments = 1
    b[27] = 0x13; // segment size = 19 bytes (OpusHead)
    // OpusHead identification header
    b[28] = 0x4F;
    b[29] = 0x70;
    b[30] = 0x75;
    b[31] = 0x73; // "Opus"
    b[32] = 0x48;
    b[33] = 0x65;
    b[34] = 0x61;
    b[35] = 0x64; // "Head"
    b[36] = 0x01; // version = 1
    b[37] = 0x02; // channel count = 2 (stereo)
    b[38] = 0x38;
    b[39] = 0x01; // pre-skip = 312 (little-endian)
    b[40] = 0x44;
    b[41] = (byte) 0xAC;
    b[42] = 0x00;
    b[43] = 0x00; // input sample rate = 44100
    b[44] = 0x00;
    b[45] = 0x00; // output gain = 0
    b[46] = 0x00; // channel mapping family = 0 (stereo)
    return b;
  }

  /**
   * Builds a minimal EPUB-compliant ZIP: a STORED "mimetype" entry with no extra field and no
   * data-descriptor, placing "application/epub+zip" at byte offset 38, exactly where the libmagic
   * EPUB rule matches. No central directory is included; some databases may fall back to
   * application/zip.
   */
  private static byte[] buildEpubBytes() {
    byte[] filename = "mimetype".getBytes(StandardCharsets.UTF_8); // 8 bytes
    byte[] content = "application/epub+zip".getBytes(StandardCharsets.UTF_8); // 20 bytes
    CRC32 crc32 = new CRC32();
    crc32.update(content);
    int crc = (int) crc32.getValue();
    // Local file header (30 bytes) + filename (8) + content (20) = 58 bytes total
    // At offset 30+0 = 30: "mimetype" | at offset 38: "application/epub+zip"
    byte[] zip = new byte[30 + filename.length + content.length];
    zip[0] = 0x50;
    zip[1] = 0x4B;
    zip[2] = 0x03;
    zip[3] = 0x04; // PK\x03\x04
    zip[4] = 0x0A;
    zip[5] = 0x00; // version needed = 1.0 (stored)
    // general purpose flags [6..7] = 0
    // compression method [8..9] = 0 (STORED)
    // mod time/date [10..13] = 0
    zip[14] = (byte) (crc & 0xFF);
    zip[15] = (byte) ((crc >> 8) & 0xFF);
    zip[16] = (byte) ((crc >> 16) & 0xFF);
    zip[17] = (byte) ((crc >> 24) & 0xFF);
    zip[18] = (byte) (content.length & 0xFF); // compressed size
    zip[22] = (byte) (content.length & 0xFF); // uncompressed size
    zip[26] = (byte) filename.length; // filename length = 8
    zip[27] = 0x00;
    zip[28] = 0x00;
    zip[29] = 0x00; // extra field length = 0 (critical, no offset shift)
    System.arraycopy(filename, 0, zip, 30, filename.length);
    System.arraycopy(content, 0, zip, 30 + filename.length, content.length);
    return zip;
  }

  /** Builds a complete ZIP file via ZipOutputStream (used for ZIP and CBZ tests). */
  private static byte[] buildZipBytes() throws IOException {
    ByteArrayOutputStream baos = new ByteArrayOutputStream();
    try (ZipOutputStream zos = new ZipOutputStream(baos)) {
      zos.putNextEntry(new ZipEntry("readme.txt"));
      zos.write("hello".getBytes(StandardCharsets.UTF_8));
      zos.closeEntry();
    }
    return baos.toByteArray();
  }

