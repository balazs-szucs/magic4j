  @Test
  void setFlagsMutatesDetectionMode() {
    try (Magic magic = Magic.open(MagicFlags.MAGIC_NONE)) {
      String description = magic.detect(PNG);
      // MAGIC_NONE returns a human-readable description, not a MIME type
      assertFalse(
          description.contains("/"),
          "MAGIC_NONE should return textual description, not a MIME type. Got: " + description);

      magic.setFlags(MagicFlags.MAGIC_MIME_TYPE);
      assertEquals(
          "image/png", magic.detect(PNG), "After setFlags(MIME_TYPE), must return MIME type");
    }
  }

